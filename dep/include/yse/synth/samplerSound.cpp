#include "samplerSound.h"

YSE::SYNTH::samplerSoundWithChannel::samplerSoundWithChannel(const String &name, AudioFormatReader &source, int channel, const BigInteger &midiNotes, int midiNoteForNormalPitch, double attackTimeSecs, double releaseTimeSecs, double maxSampleLengthSeconds)
: SamplerSound(name, source, midiNotes, midiNoteForNormalPitch, attackTimeSecs, releaseTimeSecs, maxSampleLengthSeconds)
, channel(channel) {

}

bool YSE::SYNTH::samplerSoundWithChannel::appliesToChannel(const int midiChannel) {
  // zero is omni
  if (channel == 0) return true;
  return (midiChannel == channel);
}
